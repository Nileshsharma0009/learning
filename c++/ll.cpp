vector<int > Build(string & pat){
	int n = pat.size();
   vector<int> lps(n ,0);
	int prefix=0 ;
	int suffix = 1;

	while(suffix < n){
		if(pat[prefix]==pat[suffix]){
			prefix++;
			lps[suffix]=prefix ;
			suffix;
		}
	}

}

vector<vector<int>> transpose(vector<vector<int>>& mat) {
    int   row = mat.size() , col = mat[0].size() ;
  
	for(int i = 0 ;i< row ;i++){
		for(int j = i+ 1;j < col; j++){
			swap(mat[i][j] ,mat[j][i]) ;
			
			
		}
	}
	return mat ;
}
